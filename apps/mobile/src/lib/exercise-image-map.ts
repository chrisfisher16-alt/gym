// src/lib/exercise-image-map.ts
// Maps exercise IDs to their start/end position photograph URLs
// Images from free-exercise-db (https://github.com/yuhonas/free-exercise-db) — Unlicense (public domain)
// TODO: For production, host images on CDN (Cloudflare R2 / Supabase Storage) with thumbnail variants

export interface ExerciseImageSet {
  startPosition: string; // URL to 0.jpg (starting position)
  endPosition: string;   // URL to 1.jpg (ending position)
}

const BASE_URL = 'https://raw.githubusercontent.com/yuhonas/free-exercise-db/main/exercises';

function imageSet(folder: string): ExerciseImageSet {
  return {
    startPosition: `${BASE_URL}/${folder}/0.jpg`,
    endPosition: `${BASE_URL}/${folder}/1.jpg`,
  };
}

